package model;

public class Attendee {

	private int attendeeID;
	private String name;

	public Attendee() {
	}

	public int getAttendeeID() {
		return attendeeID;
	}

	public void setAttendeeID(int attendeeID) {
		this.attendeeID = attendeeID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
